function roboBeepBoop(roboInput) {
  let roboOutput = [];
  for (let i = 0; i <= roboInput; i++) {
    if (i.toString().includes("1")); {
      roboOutput.push("Beep!");
    } if (i.toString().includes("2")); {
      roboOutput.push("Boop!");
    } 
    }
    return roboOutput;
  }

